def binToCoord(lst):
    coordinates = []
    for i in range(5):
        for j in range(5):
            if(lst[i][j] == '1'):
                coord = bin(5*i + j)[2:]
                coordinates.append((5-len(coord))*'0' + coord + '000')
    return coordinates

